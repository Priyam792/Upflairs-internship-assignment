# 2.   Create a tuple: fruits = ("apple", "banana", "mango", "orange") Print: 
# ● Second fruit  
# ● Last two fruits  
# ● Total number of fruits

#solution -->

fruits = ("apple", "banana", "mango", "orange")
print(f"Second fruit : {fruits[1]}")
print(f"Last two fruits : {fruits[-1],fruits[-2]}")
print(f"Total number of fruits = {len(fruits)}")

