# 4. Explain any five commonly used string methods in Python with examples.

# Solution :

# (a) upper() ----> Converts all characters in a string to uppercase.

# Example :

text = "hello"
print(text.upper())

# (b) lower() -----> Converts all characters in a string to lowercase.

# Example :

text = "HELLO"
print(text.lower())

# (c) capitalize() -----> Converts the first character of a string to uppercase and the rest to lowercase.

# Example :

text = "python programming"
print(text.capitalize())

# (d) replace() -----> Replaces a specified substring with another substring.

# Example :

text = "I like Java"
print(text.replace("Java", "Python"))

# (e) split() -----> Splits a string into a list based on a specified separator.

# Example :

text = "apple,banana,mango"
print(text.split(","))
