# 1. Explain the difference between the following data types with examples:
# • Integer (int)
# • Float (float)
# • String (str)
# • Boolean (bool)

# solutions :


# ------------>(a) A integer (int) is a number without a decimal point.

# For Example :

a = 5
b = 300
c = -5


# ------------>(b) A float is a number that contains a decimal point.

# For Example :

d = 0.45
e = 5.34
f = -3.54

# ------------>(c) A string (str) is a sequence of characters enclosed in sigle or double quotes.

# For Example :

name = "Priyam"
branch = "Artificial Intelligence"
roll_no = "046"

# ------------>(d) A Boolean(bool) only represents one of two values : True or False

# For Example :

is_male = True
is_female = False
